package control;

public class WhileEx1 {
    public static void main(String[] args) {
        // while (조건식) {
        // // 반복적으로 수행할 구문
        // // 증감식 또는 break 사용해야 함
        // }
        int i = 0;
        while (i < 10) {
            System.out.println(i + " 안녕하세요");
            i++;
        }
    }
}
